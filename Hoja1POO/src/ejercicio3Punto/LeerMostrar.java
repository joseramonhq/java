package ejercicio3Punto;

public class LeerMostrar {

	public static void mostrarInt(int a) {
		System.out.println(a);
	}

	public static void mostrarDouble(double a) {
		System.out.println(a);
	}

	public static void mostrarCadena(String a) {
		System.out.println(a);
	}

	public static void mostrarChar(char a) {
		System.out.println(a);
	}

	public static void mostrarLong(long a) {
		System.out.println(a);
	}

	public static void mostrarFloat(float a) {
		System.out.println(a);
	}

	public static void mostrarShort(short a) {
		System.out.println(a);
	}

	public static void mostrarByte(byte a) {
		System.out.println((byte) a);
	}

	public static void mostrarObjeto(Object a) {
		System.out.println(a);
	}

	public void mostrarObjeto() {
		System.out.println(this.toString());
	}
	
}
